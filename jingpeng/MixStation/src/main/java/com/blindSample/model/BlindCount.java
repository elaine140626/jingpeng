package com.blindSample.model;

public class BlindCount {

	private int blindCount;
	private int completedBlindCount;
	private int qualifiedBlindCount;
	public int getBlindCount() {
		return blindCount;
	}
	public void setBlindCount(int blindCount) {
		this.blindCount = blindCount;
	}
	public int getCompletedBlindCount() {
		return completedBlindCount;
	}
	public void setCompletedBlindCount(int completedBlindCount) {
		this.completedBlindCount = completedBlindCount;
	}
	public int getQualifiedBlindCount() {
		return qualifiedBlindCount;
	}
	public void setQualifiedBlindCount(int qualifiedBlindCount) {
		this.qualifiedBlindCount = qualifiedBlindCount;
	}
	
	
}
