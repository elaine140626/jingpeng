package com.blindSample.model;

//盲样明细表
public class BlindDetailed_01 {

	private Long id;	//自增长ID
	private String cement_type;	//水泥品种
	private String cement_strength_grade;	//水泥强度等级
	private Integer age;	//龄期(天)
	private String samplesize;	//试件尺寸(mm)
	private String designstrength;	//设计强度
	private String molddate;	//成型日期
	private String spectype;	//试件类型
	private String asphalttype;	//沥青种类
	private String mixturetype;	//混合料种类
	private String gradationtype;	//级配类型
	private String strength_grade;	//强度等级或牌号
	private Float diameter;	//直径
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getCement_type() {
		return cement_type;
	}
	public void setCement_type(String cement_type) {
		this.cement_type = cement_type;
	}
	public String getCement_strength_grade() {
		return cement_strength_grade;
	}
	public void setCement_strength_grade(String cement_strength_grade) {
		this.cement_strength_grade = cement_strength_grade;
	}
	public Integer getAge() {
		return age;
	}
	public void setAge(Integer age) {
		this.age = age;
	}
	public String getSamplesize() {
		return samplesize;
	}
	public void setSamplesize(String samplesize) {
		this.samplesize = samplesize;
	}
	public String getDesignstrength() {
		return designstrength;
	}
	public void setDesignstrength(String designstrength) {
		this.designstrength = designstrength;
	}
	public String getMolddate() {
		return molddate;
	}
	public void setMolddate(String molddate) {
		this.molddate = molddate;
	}
	public String getSpectype() {
		return spectype;
	}
	public void setSpectype(String spectype) {
		this.spectype = spectype;
	}
	public String getAsphalttype() {
		return asphalttype;
	}
	public void setAsphalttype(String asphalttype) {
		this.asphalttype = asphalttype;
	}
	public String getMixturetype() {
		return mixturetype;
	}
	public void setMixturetype(String mixturetype) {
		this.mixturetype = mixturetype;
	}
	public String getGradationtype() {
		return gradationtype;
	}
	public void setGradationtype(String gradationtype) {
		this.gradationtype = gradationtype;
	}
	public String getStrength_grade() {
		return strength_grade;
	}
	public void setStrength_grade(String strength_grade) {
		this.strength_grade = strength_grade;
	}
	public Float getDiameter() {
		return diameter;
	}
	public void setDiameter(Float diameter) {
		this.diameter = diameter;
	}
	
	
}
