package com.blindSample.model;

//试验操作记录表
public class TestRecordDetailed {

	private Long id;	//自增长ID
	private String serialnumber;	//试验流水号
	private String teststate;	//试验状态
	private String recordcontent;	//记录描述
	private String testoperator;	//试验员
	private String returnstate;	//试验退回至
	private String operator;	//操作人
	private String operatedate;	//操作时间
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getSerialnumber() {
		return serialnumber;
	}
	public void setSerialnumber(String serialnumber) {
		this.serialnumber = serialnumber;
	}
	public String getTeststate() {
		return teststate;
	}
	public void setTeststate(String teststate) {
		this.teststate = teststate;
	}
	public String getRecordcontent() {
		return recordcontent;
	}
	public void setRecordcontent(String recordcontent) {
		this.recordcontent = recordcontent;
	}
	public String getTestoperator() {
		return testoperator;
	}
	public void setTestoperator(String testoperator) {
		this.testoperator = testoperator;
	}
	public String getReturnstate() {
		return returnstate;
	}
	public void setReturnstate(String returnstate) {
		this.returnstate = returnstate;
	}
	public String getOperator() {
		return operator;
	}
	public void setOperator(String operator) {
		this.operator = operator;
	}
	public String getOperatedate() {
		return operatedate;
	}
	public void setOperatedate(String operatedate) {
		this.operatedate = operatedate;
	}
	
	
}
