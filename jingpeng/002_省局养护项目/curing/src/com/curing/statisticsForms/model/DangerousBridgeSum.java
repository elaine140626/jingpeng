package com.curing.statisticsForms.model;

public class DangerousBridgeSum {
	private Double EngineeringAmountSum; // 桥梁工程计划量（m）
	private Double EngineeringCountSum; // 桥梁工程计划量（座）
	private Double ImplementMSum; // 今年实施量（m)
	private Double ImplementSeatSum; // 今年实施量（座)
	private Double LeadMSum; // 超前完成（m)
	private Double LeadSeatSum; // 超前完成（座)
	private Double StartNumberSum; // 桥梁工程开工数目（座）
	private Double StartRateSum; // 桥梁工程开工率（%）
	private Double InvestmentProvinceSum; // 桥梁工程省补总投资（万元）
	private Double FinishInvestSum; // 完成投资/万元
	private Double TunnelScheduleSum; // 桥梁工程工程进度（%）

	public Double getEngineeringAmountSum() {
		return EngineeringAmountSum;
	}

	public void setEngineeringAmountSum(Double engineeringAmountSum) {
		EngineeringAmountSum = engineeringAmountSum;
	}

	public Double getEngineeringCountSum() {
		return EngineeringCountSum;
	}

	public void setEngineeringCountSum(Double engineeringCountSum) {
		EngineeringCountSum = engineeringCountSum;
	}

	public Double getImplementMSum() {
		return ImplementMSum;
	}

	public void setImplementMSum(Double implementMSum) {
		ImplementMSum = implementMSum;
	}

	public Double getImplementSeatSum() {
		return ImplementSeatSum;
	}

	public void setImplementSeatSum(Double implementSeatSum) {
		ImplementSeatSum = implementSeatSum;
	}

	public Double getLeadMSum() {
		return LeadMSum;
	}

	public void setLeadMSum(Double leadMSum) {
		LeadMSum = leadMSum;
	}

	public Double getLeadSeatSum() {
		return LeadSeatSum;
	}

	public void setLeadSeatSum(Double leadSeatSum) {
		LeadSeatSum = leadSeatSum;
	}

	public Double getStartNumberSum() {
		return StartNumberSum;
	}

	public void setStartNumberSum(Double startNumberSum) {
		StartNumberSum = startNumberSum;
	}

	public Double getStartRateSum() {
		return StartRateSum;
	}

	public void setStartRateSum(Double startRateSum) {
		StartRateSum = startRateSum;
	}

	public Double getInvestmentProvinceSum() {
		return InvestmentProvinceSum;
	}

	public void setInvestmentProvinceSum(Double investmentProvinceSum) {
		InvestmentProvinceSum = investmentProvinceSum;
	}

	public Double getFinishInvestSum() {
		return FinishInvestSum;
	}

	public void setFinishInvestSum(Double finishInvestSum) {
		FinishInvestSum = finishInvestSum;
	}

	public Double getTunnelScheduleSum() {
		return TunnelScheduleSum;
	}

	public void setTunnelScheduleSum(Double tunnelScheduleSum) {
		TunnelScheduleSum = tunnelScheduleSum;
	}

}
