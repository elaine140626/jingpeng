package com.curing.projectSchedule.model;

public class GreenNumberSum {
	private Double RouteLengthSum; // 路线长度（m）
	private Double PlantLengthSum; // 种植长度（m）
	private Double PlantingGrassSum; // 植草（m2）

	public Double getRouteLengthSum() {
		return RouteLengthSum;
	}

	public void setRouteLengthSum(Double routeLengthSum) {
		RouteLengthSum = routeLengthSum;
	}

	public Double getPlantLengthSum() {
		return PlantLengthSum;
	}

	public void setPlantLengthSum(Double plantLengthSum) {
		PlantLengthSum = plantLengthSum;
	}

	public Double getPlantingGrassSum() {
		return PlantingGrassSum;
	}

	public void setPlantingGrassSum(Double plantingGrassSum) {
		PlantingGrassSum = plantingGrassSum;
	}

}
