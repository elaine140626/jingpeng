package com.curing.projectSchedule.model;

public class DeckTreatmentNumberSum {
	private Double BridgeLengthSum; // 桥梁长度（米）
	private Double BridgeWidthSum; // 桥面净宽（米）
	private Double FineThicknessSum; // 温拌细粒厚度（厘米）
	private Double FineAreaSum; // 温拌细粒面积（千平方米）
	private Double NeutralThicknessSum; // 温拌中粒厚度（厘米）
	private Double NeutralAreaSum; // 温拌中粒面积（千平方米）
	private Double RubberAsphaltGravelSum; // 橡胶沥青碎石封层（千平方米）
	private Double DeckThicknessSum; // 铣刨桥面厚度（厘米）
	private Double DeckAreaSum; // 铣刨桥面面积（千平方米）

	public Double getBridgeLengthSum() {
		return BridgeLengthSum;
	}

	public void setBridgeLengthSum(Double bridgeLengthSum) {
		BridgeLengthSum = bridgeLengthSum;
	}

	public Double getBridgeWidthSum() {
		return BridgeWidthSum;
	}

	public void setBridgeWidthSum(Double bridgeWidthSum) {
		BridgeWidthSum = bridgeWidthSum;
	}

	public Double getFineThicknessSum() {
		return FineThicknessSum;
	}

	public void setFineThicknessSum(Double fineThicknessSum) {
		FineThicknessSum = fineThicknessSum;
	}

	public Double getFineAreaSum() {
		return FineAreaSum;
	}

	public void setFineAreaSum(Double fineAreaSum) {
		FineAreaSum = fineAreaSum;
	}

	public Double getNeutralThicknessSum() {
		return NeutralThicknessSum;
	}

	public void setNeutralThicknessSum(Double neutralThicknessSum) {
		NeutralThicknessSum = neutralThicknessSum;
	}

	public Double getNeutralAreaSum() {
		return NeutralAreaSum;
	}

	public void setNeutralAreaSum(Double neutralAreaSum) {
		NeutralAreaSum = neutralAreaSum;
	}

	public Double getRubberAsphaltGravelSum() {
		return RubberAsphaltGravelSum;
	}

	public void setRubberAsphaltGravelSum(Double rubberAsphaltGravelSum) {
		RubberAsphaltGravelSum = rubberAsphaltGravelSum;
	}

	public Double getDeckThicknessSum() {
		return DeckThicknessSum;
	}

	public void setDeckThicknessSum(Double deckThicknessSum) {
		DeckThicknessSum = deckThicknessSum;
	}

	public Double getDeckAreaSum() {
		return DeckAreaSum;
	}

	public void setDeckAreaSum(Double deckAreaSum) {
		DeckAreaSum = deckAreaSum;
	}

}
