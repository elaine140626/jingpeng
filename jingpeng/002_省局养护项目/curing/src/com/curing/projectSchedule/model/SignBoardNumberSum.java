package com.curing.projectSchedule.model;

public class SignBoardNumberSum {
	private Double SingleHangAmountSum; // 单悬标志（块）数量
	private Double SingleHangWeightSum; // 单悬标志（块）重量（Kg）
	private Double SingleColumnAmountSum; // 单柱标志（块）数量
	private Double SingleColumnWeightSum; // 单柱标志（块）重量（Kg）
	private Double DoubleColumnAmountSum; // 双柱标志（块）数量
	private Double DoubleColumnWeightSum; // 双柱标志（块）重量（Kg）
	private Double AttachmentAmountSum;// 附着标志（块）数量
	private Double AttachmentWeightSum;// 附着标志（块）重量

	public Double getSingleHangAmountSum() {
		return SingleHangAmountSum;
	}

	public void setSingleHangAmountSum(Double singleHangAmountSum) {
		SingleHangAmountSum = singleHangAmountSum;
	}

	public Double getSingleHangWeightSum() {
		return SingleHangWeightSum;
	}

	public void setSingleHangWeightSum(Double singleHangWeightSum) {
		SingleHangWeightSum = singleHangWeightSum;
	}

	public Double getSingleColumnAmountSum() {
		return SingleColumnAmountSum;
	}

	public void setSingleColumnAmountSum(Double singleColumnAmountSum) {
		SingleColumnAmountSum = singleColumnAmountSum;
	}

	public Double getSingleColumnWeightSum() {
		return SingleColumnWeightSum;
	}

	public void setSingleColumnWeightSum(Double singleColumnWeightSum) {
		SingleColumnWeightSum = singleColumnWeightSum;
	}

	public Double getDoubleColumnAmountSum() {
		return DoubleColumnAmountSum;
	}

	public void setDoubleColumnAmountSum(Double doubleColumnAmountSum) {
		DoubleColumnAmountSum = doubleColumnAmountSum;
	}

	public Double getDoubleColumnWeightSum() {
		return DoubleColumnWeightSum;
	}

	public void setDoubleColumnWeightSum(Double doubleColumnWeightSum) {
		DoubleColumnWeightSum = doubleColumnWeightSum;
	}

	public Double getAttachmentAmountSum() {
		return AttachmentAmountSum;
	}

	public void setAttachmentAmountSum(Double attachmentAmountSum) {
		AttachmentAmountSum = attachmentAmountSum;
	}

	public Double getAttachmentWeightSum() {
		return AttachmentWeightSum;
	}

	public void setAttachmentWeightSum(Double attachmentWeightSum) {
		AttachmentWeightSum = attachmentWeightSum;
	}

}
