package com.curing.projectSchedule.model;

public class RoadbedProtectionNumberSum {
	private Double WallLengthSum; // 墙长（m）
	private Double RaiseHeightSum; // 抬高高度（m）
	private Double AverageHighSum; // 平均高（H）
	private Double StoneWallBodySum; // 片石墙身（m3）
	private Double StoneBasicsSum; // 片石基础（m3）
	private Double PlasteringSum; // 抹面（m3）
	private Double GravelCushionSum; // 砂砾垫层（m3）
	private Double OpenExcavationSum; // 开挖方（m3）
	private Double LengthSum; // 长度（m）
	private Double BlockStoneSum; // 块石（m3）
	private Double EightRebarSum; // 8mm钢筋（Kg）
	private Double WireSum; // 铁丝（m)

	public Double getWallLengthSum() {
		return WallLengthSum;
	}

	public void setWallLengthSum(Double wallLengthSum) {
		WallLengthSum = wallLengthSum;
	}

	public Double getRaiseHeightSum() {
		return RaiseHeightSum;
	}

	public void setRaiseHeightSum(Double raiseHeightSum) {
		RaiseHeightSum = raiseHeightSum;
	}

	public Double getAverageHighSum() {
		return AverageHighSum;
	}

	public void setAverageHighSum(Double averageHighSum) {
		AverageHighSum = averageHighSum;
	}

	public Double getStoneWallBodySum() {
		return StoneWallBodySum;
	}

	public void setStoneWallBodySum(Double stoneWallBodySum) {
		StoneWallBodySum = stoneWallBodySum;
	}

	public Double getStoneBasicsSum() {
		return StoneBasicsSum;
	}

	public void setStoneBasicsSum(Double stoneBasicsSum) {
		StoneBasicsSum = stoneBasicsSum;
	}

	public Double getPlasteringSum() {
		return PlasteringSum;
	}

	public void setPlasteringSum(Double plasteringSum) {
		PlasteringSum = plasteringSum;
	}

	public Double getGravelCushionSum() {
		return GravelCushionSum;
	}

	public void setGravelCushionSum(Double gravelCushionSum) {
		GravelCushionSum = gravelCushionSum;
	}

	public Double getOpenExcavationSum() {
		return OpenExcavationSum;
	}

	public void setOpenExcavationSum(Double openExcavationSum) {
		OpenExcavationSum = openExcavationSum;
	}

	public Double getLengthSum() {
		return LengthSum;
	}

	public void setLengthSum(Double lengthSum) {
		LengthSum = lengthSum;
	}

	public Double getBlockStoneSum() {
		return BlockStoneSum;
	}

	public void setBlockStoneSum(Double blockStoneSum) {
		BlockStoneSum = blockStoneSum;
	}

	public Double getEightRebarSum() {
		return EightRebarSum;
	}

	public void setEightRebarSum(Double eightRebarSum) {
		EightRebarSum = eightRebarSum;
	}

	public Double getWireSum() {
		return WireSum;
	}

	public void setWireSum(Double wireSum) {
		WireSum = wireSum;
	}

}
