package com.curing.projectSchedule.model;

public class MarkingLineNumberSum {
	private Double SpecSum; // 规格
	private Double AmountSum; // 数量
	private Double RealLengthSum; // 实段长度
	private Double EmptyLengthSum; // 虚段长度
	private Double SolidLineLengthSum; // 实线长度
	private Double SolidLineAreaSum; // 实线面积
	public Double getSpecSum() {
		return SpecSum;
	}
	public void setSpecSum(Double specSum) {
		SpecSum = specSum;
	}
	public Double getAmountSum() {
		return AmountSum;
	}
	public void setAmountSum(Double amountSum) {
		AmountSum = amountSum;
	}
	public Double getRealLengthSum() {
		return RealLengthSum;
	}
	public void setRealLengthSum(Double realLengthSum) {
		RealLengthSum = realLengthSum;
	}
	public Double getEmptyLengthSum() {
		return EmptyLengthSum;
	}
	public void setEmptyLengthSum(Double emptyLengthSum) {
		EmptyLengthSum = emptyLengthSum;
	}
	public Double getSolidLineLengthSum() {
		return SolidLineLengthSum;
	}
	public void setSolidLineLengthSum(Double solidLineLengthSum) {
		SolidLineLengthSum = solidLineLengthSum;
	}
	public Double getSolidLineAreaSum() {
		return SolidLineAreaSum;
	}
	public void setSolidLineAreaSum(Double solidLineAreaSum) {
		SolidLineAreaSum = solidLineAreaSum;
	}
	
}
