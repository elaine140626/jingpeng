package com.curing.projectSchedule.model;

public class SideDitchNumberSum {
	private Double LengthSum; // 长度（m）
	private Double FoundationSum; // 挖基土方（m³）
	private Double MortarSum; // 10号砂浆抹面（㎡）
	private Double SandCushionSum; // 10cm砂垫层（m³）
	private Double RubbleSum; // 7.5号浆砌片石（m³）
	private Double C20ConcreteSum; // C20混凝土（m³）
	private Double CoverPlateSum; // 盖板（块）
	private Double EightRebarSum; // 8mm钢筋（Kg）
	private Double TwelveRebarSum; // 12mm钢筋（Kg）
	private Double C25ConcreteSum; // C25混凝土（m³）

	public Double getLengthSum() {
		return LengthSum;
	}

	public void setLengthSum(Double lengthSum) {
		LengthSum = lengthSum;
	}

	public Double getFoundationSum() {
		return FoundationSum;
	}

	public void setFoundationSum(Double foundationSum) {
		FoundationSum = foundationSum;
	}

	public Double getMortarSum() {
		return MortarSum;
	}

	public void setMortarSum(Double mortarSum) {
		MortarSum = mortarSum;
	}

	public Double getSandCushionSum() {
		return SandCushionSum;
	}

	public void setSandCushionSum(Double sandCushionSum) {
		SandCushionSum = sandCushionSum;
	}

	public Double getRubbleSum() {
		return RubbleSum;
	}

	public void setRubbleSum(Double rubbleSum) {
		RubbleSum = rubbleSum;
	}

	public Double getC20ConcreteSum() {
		return C20ConcreteSum;
	}

	public void setC20ConcreteSum(Double c20ConcreteSum) {
		C20ConcreteSum = c20ConcreteSum;
	}

	public Double getCoverPlateSum() {
		return CoverPlateSum;
	}

	public void setCoverPlateSum(Double coverPlateSum) {
		CoverPlateSum = coverPlateSum;
	}

	public Double getEightRebarSum() {
		return EightRebarSum;
	}

	public void setEightRebarSum(Double eightRebarSum) {
		EightRebarSum = eightRebarSum;
	}

	public Double getTwelveRebarSum() {
		return TwelveRebarSum;
	}

	public void setTwelveRebarSum(Double twelveRebarSum) {
		TwelveRebarSum = twelveRebarSum;
	}

	public Double getC25ConcreteSum() {
		return C25ConcreteSum;
	}

	public void setC25ConcreteSum(Double c25ConcreteSum) {
		C25ConcreteSum = c25ConcreteSum;
	}

}
