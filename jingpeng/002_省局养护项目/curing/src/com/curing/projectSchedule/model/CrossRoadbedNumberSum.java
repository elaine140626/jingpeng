	public Double getStickyLayerSum() {
		return StickyLayerSum;
	}

	public void setStickyLayerSum(Double stickyLayerSum) {
		StickyLayerSum = stickyLayerSum;
	}

	public Double getPermeableLayerSum() {
		return PermeableLayerSum;
	}

	public void setPermeableLayerSum(Double permeableLayerSum) {
		PermeableLayerSum = permeableLayerSum;
	}

