package com.curing.projectSchedule.model;

public class SevenDailySum {
	private Double ThisCompleteSum; // 本期完成（平方米）
	private Double ThisCapitalSum; // 本期完成资金

	public Double getThisCompleteSum() {
		return ThisCompleteSum;
	}

	public void setThisCompleteSum(Double thisCompleteSum) {
		ThisCompleteSum = thisCompleteSum;
	}

	public Double getThisCapitalSum() {
		return ThisCapitalSum;
	}

	public void setThisCapitalSum(Double thisCapitalSum) {
		ThisCapitalSum = thisCapitalSum;
	}

}
